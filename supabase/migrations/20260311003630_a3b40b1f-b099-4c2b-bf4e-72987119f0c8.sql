
-- Create funcionarios table
CREATE TABLE public.funcionarios (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  matricula text NOT NULL UNIQUE,
  nome text NOT NULL,
  funcao text NOT NULL,
  created_at timestamp with time zone DEFAULT now()
);

-- Enable RLS
ALTER TABLE public.funcionarios ENABLE ROW LEVEL SECURITY;

-- All authenticated can read
CREATE POLICY "Auth select funcionarios" ON public.funcionarios
  FOR SELECT TO authenticated USING (true);

-- Only admins can insert/update/delete
CREATE POLICY "Admin insert funcionarios" ON public.funcionarios
  FOR INSERT TO authenticated WITH CHECK (has_role(auth.uid(), 'admin'));

CREATE POLICY "Admin update funcionarios" ON public.funcionarios
  FOR UPDATE TO authenticated USING (has_role(auth.uid(), 'admin'));

CREATE POLICY "Admin delete funcionarios" ON public.funcionarios
  FOR DELETE TO authenticated USING (has_role(auth.uid(), 'admin'));

